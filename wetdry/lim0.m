function [g] = lim0(g)
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% VARS:
%--------------------------------------------------------------------------
fl    = g.bfl;
fr    = g.bfr;
tol   = g.drytol;
%--------------------------------------------------------------------------
Z_dof = g.x(:, :, 1);
Zbdof = g.zb;
H_dof = Z_dof-Zbdof;
H_m   = meanval(g, H_dof);
H_l   = H_dof*fl';
H_r   = H_dof*fr';
%--------------------------------------------------------------------------
log   = H_m < tol | (H_l(:, 1) < tol & H_r(:, 1) < tol);
c     = find(log);
m     = size(c, 1);
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% Loop through "problematic" cells:
%--------------------------------------------------------------------------
for i = 1:m
    o             = c   (i, 1);
    g.x (o, :, 1) = Zbdof(o, :);
    g.x (o, :, 2) = 0;
end
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
end