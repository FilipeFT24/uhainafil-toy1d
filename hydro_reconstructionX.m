function [h_jmp, zbjmp] = ...
    hydro_reconstructionX(zi, ze, zbi, zbe)
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% HYDRO RECONSTRUCTION:
hi       = zi-zbi;
zbmax    = max(zbi, zbe);
delta    = max(0, zbmax-zi);
zbtilde  = zbmax-delta;
%--------------------------------------------------------------------------
h_tildei = max(0, zi-zbmax); % =NEW hi
h_tildee = max(0, ze-zbmax); % =NEW he
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%{
h_jmp    = 1./2.*(hi-he);
hujmp    = 1./2.*(hui-hue);
z_jmp    = 1./2.*(zi-ze);
%}
%--------------------------------------------------------------------------
h_jmp    = hi-h_tildei;
zbjmp    = zbi-zbtilde;
%--------------------------------------------------------------------------
h_jmp    = 1./2.*(h_tildei-h_tildee)+h_jmp;
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
end