function [d1X] = d1Xlift_c(g, Xdof)
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% CELL:
%--------------------------------------------------------------------------
K       = g.numE;
R       = g.R;
bD      = g.bD;
X_perm  = permute(X, [2, 3, 1]);
d1Xquad = reshape(pagemtimes(bD, X_perm), [R, K])';
d1X     = d1Xquad*g.fc;
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
end
