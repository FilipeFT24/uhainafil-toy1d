function [d1X] = d1Xlift_c(g, Xdof) % continuous
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% CELL:
%--------------------------------------------------------------------------
K         = g.numE;
R         = g.R;
bD        = g.bD;
Xdof_perm = permute(Xdof, [2, 3, 1]);
d1Xquad   = reshape(pagemtimes(bD, Xdof_perm), [R, K])';
d1X       = d1Xquad*g.fc;
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
end